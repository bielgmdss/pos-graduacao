
# VARIAVEIS é um espaço na memoria do computador, utilizado para que um programa possa armazenar dados.

# - São usadas para guarda valores que serão usados mais tarde pelo programa.

# ATRIBUTOS DE UMA VARIAVEL

# - Endereço na memoria
# - Tipo = inteiro ( INT ), real ou ponto flutuante ( FLOAT ), logico ( BOOL ) ou string ( STR )
# - Identificador

