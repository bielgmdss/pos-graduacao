

# TIPO INTEIRO é numeros que não possiveis casas decimais, são numeros inteiros

# TIPO REAL / PONTO FLUTUANTE são informações que pode ter casas decimais, são numeros quebrados

# TIPO CARACTERE/STRING são informações de tipo textual

# TIPO LOGICO/ BOOLENA são informaçoes que so pode responder de duas maneiras - sim ou nao - 1 ou 2 - verdaideiro ou falso

## TIPO DE DADOS PRIMITIVOS - são aqueles que armazenam um unico valor simples ( como os tipos acima )

## TIPOS DE DADOS COMPOSTOS - são aqueles que armazem varios valores em uma unica variavel ( listas, tuplas, dicionarios e conjuntos)

# - LISTA são sequencias ordenadas de valores que podem ser modificadas
# - TUPLAS são sequencias ordenadas de valores imutaveis
# - DICIONARIO são coleções de pares chaves-valor
# - CONJUNTOS são coleções não ordenadas de elementos unicos


